const LANG_KEY = "portfolio-lang";

function setLanguage(lang) {
  const strings = translations[lang];
  if (!strings) return;

  document.documentElement.lang = lang === "en" ? "en" : "pt-BR";
  document.title = strings["meta.title"];

  const metaDesc = document.querySelector('meta[name="description"]');
  if (metaDesc) metaDesc.content = strings["meta.description"];

  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const text = strings[el.dataset.i18n];
    if (text) el.textContent = text;
  });

  document.querySelectorAll("[data-i18n-attr]").forEach((el) => {
    el.dataset.i18nAttr.split(";").forEach((pair) => {
      const [attr, key] = pair.split(":").map((s) => s.trim());
      const value = strings[key];
      if (attr && value) el.setAttribute(attr, value);
    });
  });

  const langSwitch = document.querySelector(".lang-switch");
  if (langSwitch) langSwitch.setAttribute("aria-label", strings["nav.langLabel"]);

  document.querySelectorAll(".lang-btn").forEach((btn) => {
    const active = btn.dataset.lang === lang;
    btn.classList.toggle("active", active);
    btn.setAttribute("aria-pressed", active);
  });

  localStorage.setItem(LANG_KEY, lang);
}

const savedLang = localStorage.getItem(LANG_KEY);
setLanguage(savedLang === "en" ? "en" : "pt");

document.querySelectorAll(".lang-btn").forEach((btn) => {
  btn.addEventListener("click", () => setLanguage(btn.dataset.lang));
});

const header = document.querySelector(".header");
const navToggle = document.querySelector(".nav-toggle");
const navLinks = document.querySelector(".nav-links");
const yearEl = document.getElementById("year");

yearEl.textContent = new Date().getFullYear();

window.addEventListener("scroll", () => {
  header.classList.toggle("scrolled", window.scrollY > 40);
});

navToggle.addEventListener("click", () => {
  const open = navLinks.classList.toggle("open");
  navToggle.classList.toggle("open", open);
  navToggle.setAttribute("aria-expanded", open);
});

navLinks.querySelectorAll("a").forEach((link) => {
  link.addEventListener("click", () => {
    navLinks.classList.remove("open");
    navToggle.classList.remove("open");
    navToggle.setAttribute("aria-expanded", "false");
  });
});

const revealEls = document.querySelectorAll(
  ".section-header, .about-grid, .about-photo, .card, .server-item, .lang-card, .translation-tools, .contact-links, .infra-bar"
);

revealEls.forEach((el) => el.classList.add("reveal"));

const observer = new IntersectionObserver(
  (entries) => {
    entries.forEach((entry) => {
      if (entry.isIntersecting) {
        entry.target.classList.add("visible");
        observer.unobserve(entry.target);
      }
    });
  },
  { threshold: 0.12, rootMargin: "0px 0px -40px 0px" }
);

revealEls.forEach((el) => observer.observe(el));
